// Loop for
for (let i = 1; i <= 10; i++) {
    console.log(`Aqui está a tabuada de 10: ${i} x 10 = ${i * 10}.`);
}

console.log('_______x_______');


// Loop while
let i = 1;

while (i <= 10) {
    console.log(`Aqui está a tabuada de 9: ${i} x 9 = ${i * 9}.`);
    i++;
}

console.log('_______x_______');


// Loop do while
let oito = 1;

do {
    console.log(`Aqui está a tabuada de 8: ${oito} x 8 = ${oito * 8}.`);
    oito++;

} while (oito <= 10);